package io.github.scarecraw22.utils.string;

import lombok.experimental.UtilityClass;

@UtilityClass
public class StringConsts {

    public final String DASH = "-";
    public final String DOT = ".";
    public final String EMPTY_STRING = "";
    public final String EXCLAMATION_MARK = "!";
    public final String HASH = "#";
    public final String NEW_LINE = "\n";
    public final String PLUS = "+";
    public final String EQUALS = "=";
    public final String SEMICOLON = ";";
    public final String SLASH = "/";
    public final String SPACE = " ";
    public final String QUESTION_MARK = "?";
    public final String UNDERSCORE = "_";

    public final String TRUE = "true";
    public final String FALSE = "false";

    public final String DASH_WITH_SPACES = SPACE + DASH + SPACE;
}
